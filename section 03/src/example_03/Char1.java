public class Char1 {
    public static void main(String args[]) {
        char character1, character2;
        character1 = 90;
        character2 = 'Z';

        System.out.println("We can assign characters in two way:");
        System.out.println(" * by number i.e. 90 -> " + character1);
        System.out.println(" * by letter i.e. Z  -> " + character2);
    }
}
